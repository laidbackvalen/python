'''LOGICAL OPERATORS ARE USED AS IN CONDITION LIKE "AND" "OR" "NOT"'''

#in AND both the condition should be true 
#in OR any one condition should be true 
#in NOT the condition which is not false Becomes true and true becomes false

boolean1 = True
boolean2 = False

print("the value of boolean1 and boolean2 is", (boolean1 and boolean2))  #when only both the condition is true it will return true
print("the value of boolean1 and boolean2 is", (boolean1 or boolean2))   #anyone condition should be true to return true else it will return false
print("the value of boolean1 and boolean2 is", (not boolean2))   #in NOT logical operator what is false becomes true 
 